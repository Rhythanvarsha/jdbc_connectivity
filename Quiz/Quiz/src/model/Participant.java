package model;

public class Participant {
    private int participantId; // assuming participantId is auto-generated or optional
    private String name;
    private String email;

    // Existing constructors (if any)

    // New constructor with parameters name and email
    public Participant(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public Participant(int participantId, String email) {
        this.participantId = participantId;
        this.email = email;
    }


	// Getters and Setters
    public int getParticipantId() {
        return participantId;
    }

    public void setParticipantId(int participantId) {
        this.participantId = participantId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}

